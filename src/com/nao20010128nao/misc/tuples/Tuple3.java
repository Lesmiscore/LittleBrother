package com.nao20010128nao.misc.tuples;

public class Tuple3<A, B, C> {
	final A a;
	final B b;
	final C c;

	public Tuple3(A a, B b, C c) {
		// TODO 自動生成されたコンストラクター・スタブ
		this.a = a;
		this.b = b;
		this.c = c;
	}

	public A getA() {
		return a;
	}

	public B getB() {
		return b;
	}

	public C getC() {
		return c;
	}
}
