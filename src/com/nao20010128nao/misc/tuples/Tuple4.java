package com.nao20010128nao.misc.tuples;

public class Tuple4<A, B, C, D> {
	final A a;
	final B b;
	final C c;
	final D d;

	public Tuple4(A a, B b, C c, D d) {
		// TODO 自動生成されたコンストラクター・スタブ
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}

	public A getA() {
		return a;
	}

	public B getB() {
		return b;
	}

	public C getC() {
		return c;
	}

	public D getD() {
		return d;
	}
}
